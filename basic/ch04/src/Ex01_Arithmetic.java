
public class Ex01_Arithmetic
{

	public static void main(String[] args)
	{
		int num1 = 7;
		int num2 = 3;
		
		System.out.println("num + num2 = " + (num1 + num2));
		System.out.println("num - num2 = " + (num1 - num2));
		System.out.println("num * num2 = " + (num1 * num2));
		System.out.println("num / num2 = " + (num1 / num2)); // 몫
		System.out.println("num % num2 = " + (num1 % num2)); // 나머지
	}

}
